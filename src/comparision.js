const comparisionOperation = {
    equal: function (a, b) {
        return a == b;
    },

    greater: function (a, b) {
        return a > b;
    },

    less: function (a, d) {
        return a < d;
    },

    lessThanEqual: function (a, d) {
        return a <= d;
    }
}
module.exports = comparisionOperation
